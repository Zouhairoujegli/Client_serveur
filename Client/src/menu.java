    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       if (txtid.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "SVP complete le champ de recherche");
        } else {
            if (comrech.getSelectedItem().equals("code_produit")) {
                String colon[] = {"code_produit","reference","Famile","prix","stock"};
                rs = db.fcSelectCommand(colon, "produit", "code_produit LIKE '" + txtid.getText() + "' ");
                tbl_res.setModel(new ResultSetTableModel(rs));
            } else if (comrech.getSelectedItem().equals("reference")) {
                String colon[] = {"code_produit","reference","Famile","prix","stock"};
                rs = db.fcSelectCommand(colon, "produit", "reference LIKE '" + txtid.getText() + "' ");
                tbl_res.setModel(new ResultSetTableModel(rs));
            }  else if (comrech.getSelectedItem().equals("Famile")) {
                String colon[] = {"code_produit","reference","Famile","prix","stock"};
                rs = db.fcSelectCommand(colon, "produit", "Famile LIKE '" + txtid.getText() + "' ");
                tbl_res.setModel(new ResultSetTableModel(rs));
            }  else if (comrech.getSelectedItem().equals("prix")) {
                String colon[] = {"code_produit","reference","Famile","prix","stock"};
                rs = db.fcSelectCommand(colon, "produit", "prix LIKE '" + txtid.getText() + "' ");
                tbl_res.setModel(new ResultSetTableModel(rs));
                } else if (comrech.getSelectedItem().equals("stock")) {
                String colon[] = {"code_produit","reference","Famile","prix","stock"};
                rs = db.fcSelectCommand(colon, "produit", "stock LIKE '" + txtid.getText() + "' ");
                tbl_res.setModel(new ResultSetTableModel(rs));
            
            
            
                
            
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

